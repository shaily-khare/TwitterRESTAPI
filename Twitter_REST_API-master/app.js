var express = require('express');
var http = require('http');
var path = require('path');
var favicon = require('static-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');

var routes = require('./routes');
var users = require('./routes/user');

var TwitterAPI = require('./routes/TwitterAPI')



var app = express();


// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(favicon());
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded());
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));
app.use(app.router);


app.get('/', routes.index);
app.get('/users', users.list);

app.post('/status',TwitterAPI.status);

app.post('/hometimeline',TwitterAPI.hometimeline);
app.post('/friendship',TwitterAPI.friendship);
app.get('/search',TwitterAPI.search);
app.post('/updateStatus',TwitterAPI.updatestatus);
app.get('/followers',TwitterAPI.followers);
app.get('/followerslist',TwitterAPI.followerslist);
app.get('/friendslist',TwitterAPI.friendslist);
app.get('/searchUser',TwitterAPI.searchuser);
app.get('/place',TwitterAPI.place);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
    var err = new Error('Not Found');
    err.status = 404;
    next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
    app.use(function(err, req, res, next) {
        res.status(err.status || 500);
        res.render('error', {
            message: err.message,
            error: err
        });
    });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
        message: err.message,
        error: {}
    });
});


module.exports = app;
